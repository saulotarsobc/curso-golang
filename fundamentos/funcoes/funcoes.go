package main

import "fmt"

// somaa returns the sum of two integers.
//
// Parameters:
//  a (int): The first integer to be added.
//  b (int): The second integer to be added.
// Return type:
//  int: The sum of a and b.
func somaa(a int, b int) int {
	return a + b
}

// print prints the value of an integer to the console.
//
// Parameter:
//  valor (int): The integer value to be printed.
// Return type:
//  None
func imprimir(valor int) {
	fmt.Println("valor: ", valor)
}
